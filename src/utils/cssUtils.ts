export const marginX = (value: number | string) => ({
  marginLeft: value,
  marginRight: value
})

export const marginY = (value: number | string) => ({
  marginTop: value,
  marginBottom: value
})

export const paddingX = (value: number | string) => ({
  paddingLeft: value,
  paddingRight: value
})

export const paddingY = (value: number | string) => ({
  paddingTop: value,
  paddingBottom: value
})
